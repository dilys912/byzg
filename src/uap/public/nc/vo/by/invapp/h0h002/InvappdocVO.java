	@Override
	public void setAttributeValue(String attributeName, Object value) {
		if (attributeName.startsWith("usableamountbyfree")&&!attributeName.equals("usableamountbyfree")) {
			String xh = attributeName.substring("usableamountbyfree".length());
			int idx = Integer.parseInt(xh);
			boolean bValue = value == null ? false : ((UFBoolean) value).booleanValue();
			StringBuffer buffer = new StringBuffer(getUsableamountbyfree());
			buffer.replace(idx, idx + 1, bValue ? "Y" : "N");
			setUsableamountbyfree(buffer.toString());
		} else if (attributeName.startsWith("usableamount") && (attributeName.length() == 13 || attributeName.length() == 14)) {
			String xh = attributeName.substring("usableamount".length());
			int idx = Integer.parseInt(xh);
			boolean bValue = value == null ? false : ((UFBoolean) value).booleanValue();
			StringBuffer buffer = new StringBuffer(getUsableAmount());
			buffer.replace(idx, idx + 1, bValue ? "1" : "0");
			setUsableAmount(buffer.toString());
		} else if (attributeName.equals("sfrpc")) {
			producemethod = (value == null || value.toString().equalsIgnoreCase("N") || value.toString().equalsIgnoreCase("false")) ? new Integer(0) : new Integer(1);
		} else if (attributeName.equals("stockbycheck")) {
			chkStockByCheck = value == null ? new UFBoolean(false) : (UFBoolean) value;
		} else{
			super.setAttributeValue(attributeName, value);
		}
	}
	
	@Override
	public Object getAttributeValue(String attributeName) {
		if (attributeName.startsWith("usableamountbyfree")&&!attributeName.equals("usableamountbyfree")) {
			String xh = attributeName.substring("usableamountbyfree".length());
			int idx = Integer.parseInt(xh);
			return new UFBoolean(getUsableamountbyfree().charAt(idx));
		} else if (attributeName.startsWith("usableamount") && (attributeName.length() == 13 || attributeName.length() == 14)) {
			String xh = attributeName.substring("usableamount".length());
			int idx = Integer.parseInt(xh);
			return new UFBoolean(getUsableAmount().charAt(idx) == '1');
		} else if (attributeName.equals("sfrpc")) {
			return (producemethod == null || producemethod.intValue() == 0) ? new UFBoolean(false) : new UFBoolean(true);
		} else if (attributeName.equals("stockbycheck")) {
			return chkStockByCheck;
		} else{
			return super.getAttributeValue(attributeName);
		}
	}


